/** Index articles by sourceType:sourceId for fast lookup */
export const articlesBySource = new Map<string, Article[]>();
for (const article of articles) {
  const key = `${article.sourceType}:${article.sourceId}`;
  const existing = articlesBySource.get(key);
  if (existing) {
    existing.push(article);
  } else {
    articlesBySource.set(key, [article]);
  }
}

export function getArticles(
  sourceType: string,
  sourceId: number,
): Article[] {
  return articlesBySource.get(`${sourceType}:${sourceId}`) ?? [];
}
